    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
       String maHKTamTru = txtMaHKTT.getText();
       String hoTen = txtHoTen.getText();
       String soCMND = txtSoCMND.getText();
       String ngaySinh ;
       String gioiTinh ;
       String ngayChuyenDen ;
       String ngayChuyenDi ;
       String queQuan = txtQueQuan.getText();
       String diaChiTT = txtDiaChiTT.getText();
       String lyDo = txtLyDo.getText();
       String ghiChu = txtGhiChu.getText();
       String sdt = txtSDT.getText();
        SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
        ngaySinh = date.format(jdNgaySinh.getDate());
        ngayChuyenDen = date.format(jdNgayChuyenDen.getDate());
        ngayChuyenDi = date.format(jdNgayChuyenDi.getDate());
       try {
            if(rNam.isSelected()) {
                    gioiTinh = "Nam";
                }
            else {
                    gioiTinh = "Nữ";
                }
            Connection con=(Connection) new MysqlConnection().getMysqlConnection();
           Statement s = (Statement) con.createStatement();
//           s.executeUpdate("INSERT INTO tamtru(HoTen, SoCMND, NgaySinh, GioiTinh,QueQuan, DiaChiThuongTru, NgayChuyenDen, NgayChuyenDi, LyDoTamTru, SDT, GhiChu)"
//                   + " VALUES ('"+hoTen+"','"+soCMND+"','"+ngaySinh+"','"+gioiTinh+"','"+queQuan+"',,'"+diaChiTT+"','"+ngayChuyenDen+"','"+ngayChuyenDi+"','"+lyDo+"','"+sdt+"','"+ghiChu+"')");
s.executeUpdate("INSERT INTO tam_tru(MaHoKhauTamTru,HoTen, SoCMND, NgaySinh, GioiTinh,QueQuan, DiaChiThuongTru, NgayChuyenDen, NgayChuyenDi, LyDoTamTru, SoDT, GhiChu)"
                   + " VALUES ('"+maHKTamTru+"','"+hoTen+"','"+soCMND+"','"+ngaySinh+"','"+gioiTinh+"','"+queQuan+"','"+diaChiTT+"','"+ngayChuyenDen+"','"+ngayChuyenDi+"','"+lyDo+"','"+sdt+"','"+ghiChu+"')");
        JOptionPane.showMessageDialog(null, "Đăng ký thành công!");
        reset();
       }
       catch(Exception ex) {
           JOptionPane.showMessageDialog(null, "Đăng ký thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
           System.out.println(ex);
       }
    }//GEN-LAST:event_btnDangKyActionPerformed

